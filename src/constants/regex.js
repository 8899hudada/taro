/**
 * 常用正则
 */

export default {
  username: /^[0-9a-zA-Z_]{6,16}$/, // 用户名
  phone: /^1\d{10}$/, // 手机号为1开头的11位数字
  password: /^[0-9a-zA-Z`~!@#\$%\^&\*\(\)-_\+=\{\}\[\];:"'<,>\.\\\?\/·！￥%…（）——【】、\|，。《》？：；‘“]{6,16}$/, // 密码为数字、字母或特殊字符
  decimal2Reg: /^\d{1,10}(\.\d{1,2})?$/, // 至多两位小数的正实数（金额）
  idCardReg: /(^[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$)|(^[1-9]\d{5}\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{2}$)/, //身份证号
}